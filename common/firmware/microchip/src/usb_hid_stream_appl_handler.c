/********************************************************************
 * Function:        void ProcessIO(void)
 *
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Side Effects:    None
 *
 * Overview:        This function is a place holder for other user
 *                  routines. It is a mixture of both USB and
 *                  non-USB tasks.
 *
 * Note:            None
 *******************************************************************/
void ProcessIO(void)
{
    u16 txSize;

    // User Application USB tasks
    if((USBDeviceState < CONFIGURED_STATE)||(USBSuspendControl==1)) 
    {
        //USB_LOG("USBDeviceState=%hhx, USBSuspendControl=%hhx\n", USBDeviceState, USBSuspendControl);
        return;
    }

    /* read out data from usb hid, and move it to module-local buffer */
    if ( usbStreamReceive( ReceivedDataBuffer ) > 0 )
    {  /* if we have at least one fully received packet, we start execution */
	 
        /* interpret one (or more) packets in the module-local buffer */
	txSize = usbStreamProcessReceivedPackets( );

	/* transmit any data waiting in the module-local buffer over usb hid */
	usbStreamTransmit( txSize, ToSendDataBuffer );
    }

    /* we need to call the processCyclic function for all applications that
       have any data to send (without receiving a hid packet). The data to
       be sent is written into the module-local buffer */ 
    txSize = usbStreamProcessCyclic( );

    /* transmit any data waiting in the module-local buffer over usb hid */
    usbStreamTransmit( txSize, ToSendDataBuffer );

}//end ProcessIO



